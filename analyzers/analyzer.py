from abc import ABC, abstractmethod
from typing import Dict, Any
"""
    Модуль analyzer.py

    Данный модуль определяет абстрактный базовый класс Analyzer для унификации
    интерфейса анализа данных, полученных из различных источников (например,
    Proxmox API, HTTP-сервисов, файлов). Предназначен для реализации подклассов,
    которые выполняют специфическую логику анализа, включая извлечение значимых
    метрик, выявление изменений и формирование структурированных отчётов.
    Сам по себе не содержит конкретной аналитики — предоставляет каркас для
    реализации в подклассах.

    Классы:
    --------
    - Analyzer: Абстрактный базовый класс для анализаторов данных.
      Определяет интерфейс (метод analyze) и может содержать вспомогательные
      методы для сравнения состояний.

    Ключевые зависимости:
    ----------------------
    - abc: Используется для создания абстрактных базовых классов.
    - typing: Для аннотаций типов (Dict, Any).

    Пример использования:
    ----------------------
    1. Создайте подкласс от Analyzer и реализуйте метод analyze с необходимой
       логикой (например, парсинг JSON, фильтрация, сравнение с предыдущими данными).
    2. При необходимости переопределите метод compare_statuses для специфического
       сравнения состояний.
    3. Вызовите analyze(data) и обработайте полученный результат.

    Пример реализации подкласса:
        class CustomAnalyzer(Analyzer):
            def __init__(self, previous_data=None):
                self.previous_data = previous_data

            def analyze(self, data: Dict[str, Any]) -> Dict[str, Any]:
                # ... логика анализа ...
                return {"result": ...}

            def compare_statuses(self, current_data: Dict[str, Any]) -> Dict[str, Any]:
                # ... логика сравнения ...
                return {"changes": ...}
"""


class Analyzer(ABC):
    """
    Базовый анализатор данных.

    Кратко:
        Абстрактный класс, задающий интерфейс для всех анализаторов,
        которые обрабатывают входные данные (обычно словарь, полученный
        от загрузчика) и возвращают структурированный результат анализа.

    Args:
        Не требует аргументов конструктора. Подклассы могут добавлять
        собственные параметры инициализации (например, предыдущее состояние,
        настройки фильтрации).

    Attributes:
        Нет обязательных атрибутов. Подклассы могут определять любые
        необходимые поля (например, previous_data для хранения предыдущего
        состояния).

    Public methods:
        analyze(data: Dict[str, Any]) -> Dict[str, Any]:
            Абстрактный метод, который должен быть реализован в подклассе.
            Выполняет анализ переданных данных и возвращает словарь с
            результатами (структура определяется подклассом).

        compare_statuses(current_data: Dict[str, Any]) -> Dict[str, Any]:
            Метод, предназначенный для сравнения текущего состояния с предыдущим.
            В базовом классе не содержит реализации (pass). Подклассы могут
            переопределять его для выполнения специфического сравнения.

    Behavior notes:
        - Класс не навязывает формат возвращаемого словаря; это зависит от
          конкретной реализации.
        - Рекомендуется в подклассах сохранять предыдущее состояние (например,
          в атрибуте self.previous_data) для последующих сравнений.
        - Метод compare_statuses может быть использован как вспомогательный
          внутри analyze или вызываться отдельно.

    Ключевые зависимости:
        - Нет прямых зависимостей, но подклассы могут использовать любые
          библиотеки для анализа (pandas, numpy, и т.д.).

    Пример использования (в подклассе):
        class MetricAnalyzer(Analyzer):
            def __init__(self, threshold=10):
                self.threshold = threshold
                self.previous = None

            def analyze(self, data: Dict[str, Any]) -> Dict[str, Any]:
                # ... анализ данных ...
                result = {"value": data.get("metric", 0)}
                if self.previous is not None:
                    changes = self.compare_statuses(result)
                    result["changes"] = changes
                self.previous = result
                return result

            def compare_statuses(self, current_data: Dict[str, Any]) -> Dict[str, Any]:
                return {"delta": current_data["value"] - self.previous["value"]}
    """
    @abstractmethod
    def analyze(self, data: Dict[str, Any]) -> Dict[str, Any]:
        """
        Выполняет анализ входных данных.

        Этот метод предназначен для переопределения в подклассах.
        В базовой реализации не выполняет никаких действий.

        Args:
            data (Dict[str, Any]): Словарь с входными данными (обычно результат
                работы загрузчика, например HttpLoader или ProxmoxLoader).

        Returns:
            Dict[str, Any]: Словарь с результатами анализа. Структура
                определяется конкретным подклассом. Рекомендуется включать
                ключи, отражающие суть анализа (например, "changes", "metrics",
                "status").

        Raises:
            NotImplementedError: если метод не переопределён в подклассе.
        """
        pass

    def compare_statuses(self, current_data: Dict[str, Any]) -> Dict[str, Any]:
        """
        Сравнивает текущее состояние с предыдущим.

        Этот метод предназначен для переопределения в подклассах.
        В базовой реализации не выполняет никаких действий.

        Args:
            current_data (Dict[str, Any]): Словарь с текущими данными
                (обычно часть результата анализа, например, информация о
                виртуальных машинах).

        Returns:
            Dict[str, Any]: Словарь с результатами сравнения (структура
                определяется подклассом). В базовой реализации возвращает
                пустой словарь.

        Note:
            Подклассы могут использовать этот метод внутри analyze для
            выявления изменений между последовательными вызовами.
        """
