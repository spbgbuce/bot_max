"""
    Модуль formatter.py

    Данный модуль определяет абстрактный базовый класс Formatter для унификации
    форматирования данных в строковые сообщения. Предназначен для реализации
    подклассов, которые преобразуют структурированные данные (обычно словари)
    в читаемые текстовые отчёты, например, для отправки в чаты или логи.
    Сам по себе не содержит логики форматирования — предоставляет каркас для
    реализации в подклассах.

    Классы:
    --------
    - Formatter: Абстрактный базовый класс для форматтеров.
      Содержит единственный абстрактный метод format(data) -> str.

    Ключевые зависимости:
    ----------------------
    - abc: Для создания абстрактных базовых классов.
    - typing: Для аннотаций типов.

    Пример использования:
    ----------------------
    1. Создайте подкласс от Formatter и реализуйте метод format.
    2. Передайте данные (словарь) в форматтер и получите строку.
    3. Используйте результат для отправки, логирования или отображения.

    Пример реализации подкласса:
        class HttpFormatter(Formatter):
            def format(self, data: Dict) -> str:
                url = data.get("url")
                status = data.get("status")
                return f"Сайт {url} вернул статус {status}"
"""
from abc import ABC, abstractmethod
from typing import Dict


class Formatter(ABC):
    """
    Базовый форматтер данных.

    Кратко:
        Абстрактный класс, задающий интерфейс для преобразования входных данных
        (словарь) в строковое представление. Предназначен для использования в
        воркерах и других компонентах, где требуется единообразное форматирование
        информации.

    Public methods:
        format(data: Dict) -> str:
            Абстрактный метод, который должен быть реализован в подклассе.
            Принимает словарь с данными и возвращает форматированную строку.

    Behavior notes:
        - Подклассы должны реализовывать конкретную логику форматирования.
        - Входной словарь может иметь произвольную структуру, подкласс
          должен корректно обрабатывать отсутствие ключей.
        - Возвращаемая строка может содержать маркдаун, эмодзи или просто текст.

    Raises:
        NotImplementedError: если метод не переопределён в подклассе.

    Пример использования (в подклассе):
        class ProxmoxFormatter(Formatter):
            def format(self, data: Dict) -> str:
                vms = data.get("vms", [])
                return "\n".join(f"{vm['name']} – {vm['status']}" for vm in vms)
    """
    @abstractmethod
    def format(self, data: Dict) -> str:
        """
        Преобразует входные данные в строку.

        Args:
            data (Dict): Словарь с данными для форматирования.

        Returns:
            str: Форматированное строковое представление данных.

        Raises:
            NotImplementedError: если метод не переопределён в подклассе.
        """
