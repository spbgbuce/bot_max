"""
    Модуль запуска приложения (main.py)

    Данный модуль является точкой входа в систему мониторинга. Он выполняет
    настройку базового логирования (формат, уровень, вывод в консоль) и
    импортирует модуль bot, который уже инициализирует и запускает процессор.
    Такой подход позволяет централизованно управлять настройками логирования
    перед запуском основной логики.

    Содержимое:
    -----------
    - Конфигурация логирования через logging.basicConfig:
        уровень INFO, формат с временем, уровнем, именем логгера и сообщением.
    - Импорт модуля bot, который запускает процесс мониторинга.

    Ключевые зависимости:
    ----------------------
    - bot: Модуль, содержащий точку входа в приложение (создание и запуск Processor).
    - logging: Встроенная библиотека для настройки вывода логов.

    Пример запуска:
    ---------------
    # В командной строке:
    python main.py

    Примечания:
    -----------
    - Модуль bot должен находиться в той же директории или быть доступным в PYTHONPATH.
    - После запуска программа работает бесконечно до прерывания (Ctrl+C).
    - Настройки логирования применяются глобально и влияют на все модули,
      использующие логгеры.
"""
